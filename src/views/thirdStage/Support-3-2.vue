                              還好，我有一個能夠理解我、知道我多麼需要喘息的老公，在我沒有任何收入的情況下，支持我找保母、願意支付保母費用。也有人質疑我，既然請了育嬰假，怎麼還請保母照顧老二，我想不少人會覺得我很爽吧。
                            </li>
