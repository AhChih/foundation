                              這是我某天在臉書上抒發的文字，不是討拍文，只是想讓大家知道，母職不是天生，而「被懂」比什麼都來的重要……。我在育嬰假的這段期間，我深刻體認到一個全職媽媽24小時育兒是一件多麼值得被尊敬的工作。
                              <br><br>
                              回到職場，我是一位負責「青少女懷孕」及「青少年父母支持服務方案」的社工。每次替留養小媽媽媒合保母時，都會感受到他們除了要學習扮演好母親這個角色外，還需要花更多的力氣去證明他們「不是不負責任的父母」。
